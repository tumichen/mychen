package reuse_class.constructor_with_parameters;

class Meal {
    Meal(){
        System.out.println("Meal");
    }
}
class Bread {
    Bread(){
        System.out.println("Bread.Bread");
    }
}
class Cheese{
    Cheese(){
        System.out.println("Cheese.Cheese");
    }
}
class Lunch{
    Lunch(){
        System.out.println("Lunch.Lunch");
    }
}
